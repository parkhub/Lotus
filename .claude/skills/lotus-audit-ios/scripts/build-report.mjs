// Recommendations
out('## Recommendations');
out('');
out('### 🚨 Migrate iOS asset catalog from Display-P3 to sRGB');
out('');
out('**Why:** Figma, Android (Compose `Color(0xFF…)`), and Web (CSS hex) all use sRGB. iOS is the only platform tagging colours as `display-p3`. The result is a brand that looks subtly different across platforms, and visibly different across screens within the iOS app (Lotus P3 surfaces vs legacy sRGB surfaces — see finding #5).');
out('');
out('**What:** Re-tag every colorset under `Lotus/Sources/Lotus/Assets.xcassets/Lotus-Colour-Pallet/` from `"color-space": "display-p3"` to `"color-space": "srgb"`. Keep the same hex byte values. Validate visually on a real P3 iPhone afterwards — most JustPark colours are inside the sRGB gamut so the visual change is small (saturated greens become slightly less vivid; neutrals are virtually identical).');
out('');
out('**Mechanical fix:**');
out('');
out('```bash');
out('cd parkhub/ios/Lotus/Sources/Lotus/Assets.xcassets/Lotus-Colour-Pallet');
out('find . -name "Contents.json" -exec sed -i \'\' \'s/"display-p3"/"srgb"/g\' {} +');
out('# QA on a real P3-capable device before merging');
out('```');
out('');
out('Trade-off accepted: brand colours won\'t exploit the P3 gamut on modern iPhones. In exchange, the brand renders identically across iOS / Android / Web and matches Figma exactly. For a parking app this is the right call.');
out('');
out('### Other recommendations (lower priority)');
out('');
out('- **Fix `Action.PrimaryDefault`** — bring it back to the brand green (or grey-900 if following Figma\'s newer Action layer).');
out('- **Backfill Dark Mode variants** for the 22 colorsets currently missing one.');
out('- **Decide on the `Tertiary` action layer** — adopt Figma\'s expanded semantic system on iOS, or push back on Figma to drop the unused tokens.');
out('- **Migrate legacy UIKit headers off `.jpGreen`** — this is the biggest single source of brand-green inconsistency in the app.');
out('- **Update `radiusFull` to 999** to match Figma + Web.');
out('');
