out('');
out('#### Mismatches (light + dark differ)');
out('');
out('| Figma | iOS asset | Figma Light | Figma Dark | iOS Light (displayed/raw) | iOS Dark (displayed/raw) |');
out('|---|---|---|---|---|---|');
for (const r of parity.rows.filter(x => x.status === 'mismatch')) {
  const e = enrichIos(r.iosAsset);
  const lDisp = e?.light || '—', lRaw = e?.lightRaw || '—';
  const dDisp = e?.dark || '—', dRaw = e?.darkRaw || '—';
  out(`| ${r.figmaName} | ${r.iosAsset} | \`${r.figmaLight}\` | \`${r.figmaDark || '—'}\` | \`${lDisp}\` (raw \`${lRaw}\`) | \`${dDisp}\` (raw \`${dRaw}\`) |`);
}
out('');
out('#### Light matches, Dark mismatches');
out('');
out('| Figma | iOS asset | Light (✅) | Figma Dark | iOS Dark |');
out('|---|---|---|---|---|');
for (const r of parity.rows.filter(x => x.status === 'dark-only-mismatch')) {
  out(`| ${r.figmaName} | ${r.iosAsset} | \`${r.figmaLight}\` | \`${r.figmaDark}\` | \`${r.iosDark || '—'}\` |`);
}
out('');
out('#### Missing in iOS (Figma has them, iOS does not)');
out('');
const missingByFamily = {};
for (const r of parity.rows.filter(x => x.status === 'missing-ios')) {
  const fam = r.figmaName.split('/')[0];
  (missingByFamily[fam] = missingByFamily[fam] || []).push(r);
}
for (const [fam, rows] of Object.entries(missingByFamily)) {
  out(`**${fam}** — ${rows.length} missing:`);
  for (const r of rows) out(`- \`${r.figmaName}\` (Light \`${r.figmaLight}\`, Dark \`${r.figmaDark}\`)`);
  out('');
}

out('#### iOS-only (no Figma semantic equivalent)');
out('');
for (const c of parity.iosOnlyTokens) {
  out(`- \`${c.name}\` — Light \`${c.light}\` (raw \`${c.lightRaw}\`)${c.dark ? `, Dark \`${c.dark}\`` : ' — **no dark variant**'}`);
}
out('');

// Padding
out('### Padding (spacing)');
out('');
out('| Figma var | Figma value | iOS const | iOS value | Status |');
out('|---|---|---|---|---|');
for (const r of padRows) out(`| ${r.figma || '—'} | ${r.figmaVal !== null && r.figmaVal !== undefined ? r.figmaVal : '—'} | ${r.iosName || '—'} | ${r.iosVal !== null && r.iosVal !== undefined ? r.iosVal : '—'} | ${padStatus(r)} |`);
out('');
out('### Corner radius');
out('');
