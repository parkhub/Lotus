out(`7. **Per-flow adoption is bimodal.** SwiftUI-first flows (Home ${Math.round(scan.byFlow.Home?.ratio * 100 || 0)}%, On-Demand ${Math.round(scan.byFlow['On-Demand']?.ratio * 100 || 0)}%, Auth ${Math.round(scan.byFlow['Auth & Onboarding']?.ratio * 100 || 0)}%) are well tokenised. UIKit-legacy areas (Shared & Infra ${Math.round(scan.byFlow['Shared & Infra']?.ratio * 100 || 0)}%, Account ${Math.round(scan.byFlow.Account?.ratio * 100 || 0)}%, Widget ${Math.round(scan.byFlow['Widget & Extensions']?.ratio * 100 || 0)}%) are not.`);
