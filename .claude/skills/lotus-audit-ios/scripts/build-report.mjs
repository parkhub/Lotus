// Assemble the audit report markdown and write to primary + (optional) mirror.
// Usage: build-report.mjs [--out <path>]
//   inputs (env vars override):
//     FIGMA_JSON     /tmp/lotus-figma-tokens.json
//     IOS_COLORS     /tmp/ios-colors.json
//     PARITY_JSON    /tmp/colour-parity.json
//     SCAN_JSON      /tmp/ios-scan.json
//   output:
//     primary:       <lotus-repo>/audits/ios/YYYY-MM-DD.md (relative to this script)
//     mirror:        $LOTUS_AUDIT_MIRROR/YYYY-MM-DD.md (if set + dir exists)
//     index update:  Olly's vault _Index.md (only when mirror is in his vault)
import { readFileSync, writeFileSync, mkdirSync, existsSync, copyFileSync } from 'node:fs';
import { execSync } from 'node:child_process';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const figma  = JSON.parse(readFileSync(process.env.FIGMA_JSON   || '/tmp/lotus-figma-tokens.json', 'utf8'));
const ios    = JSON.parse(readFileSync(process.env.IOS_COLORS   || '/tmp/ios-colors.json', 'utf8'));
const parity = JSON.parse(readFileSync(process.env.PARITY_JSON  || '/tmp/colour-parity.json', 'utf8'));
const scan   = JSON.parse(readFileSync(process.env.SCAN_JSON    || '/tmp/ios-scan.json', 'utf8'));

// --out override
let outOverride = null;
const outIdx = process.argv.indexOf('--out');
if (outIdx !== -1 && process.argv[outIdx + 1]) outOverride = process.argv[outIdx + 1];

const fc = name => figma.collections.find(c => c.collection === name);
const colourSem = fc('Colour');
const padding = fc('Padding');
const radius = fc('Corner Radius');

const iosSpacing = [['spacingNone',0],['spacingXXXSmall',2],['spacingXXSmall',4],['spacingXSmall',8],['spacingSemiSmall',10],['spacingSmall',12],['spacingMedium',16],['spacingXMedium',20],['spacingLarge',24],['spacingXLarge',32],['spacingXXLarge',40],['spacingXXXLarge',70]];
const iosRadius = [['radiusNone',0],['radiusXXSmall',1],['radiusXSmall',4],['radiusSmall',8],['radiusXMedium',12],['radiusMedium',16],['radiusXLarge',20],['radiusLarge',24],['radiusFull',32]];
const iosTypography = [['h1Nunito','NunitoSans-Bold',32],['h1Poppins','Poppins-Bold',32],['h2Nunito','NunitoSans-Bold',24],['h2PoppinsLight','Poppins-Light',23],['h2Poppins','Poppins-Regular',24],['h2PoppinsBold','Poppins-SemiBold',24],['h3','NunitoSans-Regular',20],['bodyLarge','NunitoSans-Regular',18],['bodyLargeBold','NunitoSans-Bold',18],['bodyMedium','NunitoSans-Regular',16],['bodyMediumBold','NunitoSans-Bold',16],['bodySmall','NunitoSans-Regular',14],['bodySmallMedium','NunitoSans-Regular medium',14],['bodySmallBold','NunitoSans-Bold',14],['caption','NunitoSans-Regular',12],['captionBold','NunitoSans-Bold',12],['micro','NunitoSans-Regular',10],['microBold','NunitoSans-Bold',10],['sfSemiBoldFont','System semibold',15],['sfRegularFont','System regular',15]];

// Padding parity
const padRows = [];
for (const v of padding.vars) {
  padRows.push({ figma: v.name, figmaVal: Object.values(v.valuesByMode)[0]?.resolved, iosName: null, iosVal: null });
}
for (const [n, val] of iosSpacing) {
  const fig = padRows.find(r => r.figmaVal == val && !r.iosName);
  if (fig) { fig.iosName = n; fig.iosVal = val; }
  else padRows.push({ figma: null, figmaVal: null, iosName: n, iosVal: val });
}
const radRows = [];
for (const v of radius.vars) radRows.push({ figma: v.name, figmaVal: Object.values(v.valuesByMode)[0]?.resolved, iosName: null, iosVal: null });
for (const [n, val] of iosRadius) {
  const fig = radRows.find(r => r.figmaVal == val && !r.iosName);
  if (fig) { fig.iosName = n; fig.iosVal = val; }
  else radRows.push({ figma: null, figmaVal: null, iosName: n, iosVal: val });
}

const cs = parity.summary;
const padStatus = r => {
  if (r.figmaVal === r.iosVal && r.figma && r.iosName) return '✅ match';
  if (r.figma && !r.iosName) return '❌ missing in iOS';
  if (!r.figma && r.iosName) return '⚠️ iOS-only';
  return '❌ mismatch';
};

const today = new Date().toISOString().slice(0, 10);
const lines = [];
const out = (s='') => lines.push(s);

// Helper: enrich rows with iOS raw/displayed hex
const iosByName = Object.fromEntries(ios.map(c => [c.name.toLowerCase().replace(/[-\s]/g,''), c]));
function enrichIos(asset) {
  return iosByName[asset.toLowerCase().replace(/[-\s]/g,'')];
}

out(`# Lotus — iOS Audit (${today})`);
out('');
out(`*Auto-generated by \`/lotus-audit-ios\`. Source of truth: [Lotus design system Figma file](https://www.figma.com/design/YkS9s6Cz3EYdbr5UThzUKP/Lotus---Design-System).*`);
out('');
out(`*iOS commit:* \`${scan.iosCommit.slice(0, 7)}\` · *Figma snapshot:* \`${figma.generatedAt}\` · *Files scanned:* ${scan.totals.files}`);
out('');
out('> [!info] Colour-space note');
out('> iOS asset catalogs at JustPark are tagged `display-p3`. Figma, Android (`Color(0xFF…)`), and Web (CSS hex) all default to **sRGB**. The iOS values in this report are converted from P3 → sRGB before comparison so the numbers match what users actually see on a P3-capable iPhone. The "raw" values stored in the asset catalog are listed alongside for transparency.');
out('');
out('---');
out('');

// Summary
