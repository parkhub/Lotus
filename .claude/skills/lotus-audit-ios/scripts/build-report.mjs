out('### 🔴 Critical');
out('');
out(`1. **\`Brand.JustparkGreen\` displays as \`#00A71D\` on P3-capable iPhones**, not \`#23A437\` as designed. The asset stores bytes \`0x23 0xA4 0x37\` but is tagged \`display-p3\`, so the OS interprets those numbers in the wider P3 gamut and renders a more saturated green than Figma intended. This is the visible "really saturated header" effect on Lotus-tokenised SwiftUI screens (e.g. ReservationV2) that doesn't show on legacy UIKit screens (which use \`.jpGreen\` = \`#1FA637\` raw RGB in extended sRGB).`);
