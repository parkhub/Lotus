// Mirror only when LOTUS_AUDIT_MIRROR is explicitly set — no magic vault detection.
// The vault-index update below will only fire if the mirror happens to land in
// the Olly-vault Audits folder, but that's a specific path match, not auto-discovery.
const MIRROR_DIR = process.env.LOTUS_AUDIT_MIRROR || null;
const VAULT_AUDITS = `${process.env.HOME}/Documents/Olly's Brain/Areas/JustPark/Projects/Lotus/Audits`;
if (MIRROR_DIR && existsSync(MIRROR_DIR)) {
  let mirror = path.join(MIRROR_DIR, `${DATE}.md`);
  const mbase = mirror.replace(/\.md$/, '');
  let j = 2;
  while (existsSync(mirror)) { mirror = `${mbase}-${j}.md`; j++; }
  copyFileSync(primary, mirror);
  console.log(`mirror: ${mirror}`);

  const indexPath = `${process.env.HOME}/Documents/Olly's Brain/Areas/JustPark/Projects/Lotus/_Index.md`;
  if (existsSync(indexPath) && mirror.startsWith(VAULT_AUDITS)) {
    const mirrorName = path.basename(mirror, '.md');
    const idx = readFileSync(indexPath, 'utf8');
    if (!idx.includes(`[[Audits/${mirrorName}]]`)) {
      const updated = idx.replace(/(## Audits\n(?:- .+\n)*)/, `$1- [[Audits/${mirrorName}]] — auto-generated\n`);
      writeFileSync(indexPath, updated);
      console.log(`index updated`);
    }
  }
}
