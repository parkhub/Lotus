out('## Summary');
out('');
out('> [!summary]');
out(`> Lotus tokenisation across the iOS app sits at **${ratio}%** (${scan.totals.tokenised} tokenised refs vs ${scan.totals.violations} violations across ${scan.totals.files} UI-styling files). The headline drag is **${scan.totals.legacy} references** to the legacy \`jp*\` colour palette in \`Shared/JPCore/Extensions/UIColor+JP.swift\` — concentrated in legacy UIKit screens that pre-date Lotus. New SwiftUI flows (Home, On-Demand, Auth) are 80–90% tokenised; legacy UIKit areas (Shared & Infra, Account, Widget) are below 10%. Token-parity vs Figma is messy: of ${cs.totalFigmaSemantic} matchable Figma semantic colours, **${cs.match}** match cleanly, ${cs.mismatch} drift in value, ${cs.darkOnlyMismatch} match light but disagree in dark, and ${cs.missingIos} Figma tokens have no iOS equivalent at all (mostly the new \`Tertiary\`, \`Tapped\`, and \`Link-*\` semantic layer Figma added without iOS catching up). The brand green \`Brand.JustparkGreen\` is stored as bytes \`0x23 0xA4 0x37\` but rendered through P3 displays as \`#00A71D\` — visibly more saturated than the Figma reference \`#23A437\`. The single most critical bug — \`Action.PrimaryDefault\` resolving to \`#008000\` — is still in the asset catalog.`);
out('');
out('---');
out('');

// Token parity
out('## Token parity (Figma ↔ iOS)');
out('');
out('### Colours');
out('');
out(`Figma's semantic Colour collection has ${colourSem.vars.length} variables across Light + Dark modes. iOS has ${ios.length} colorsets in \`Lotus-Colour-Pallet\`. iOS values are shown as **displayed sRGB** (the P3-converted equivalent) with the **raw bytes** in parentheses for transparency.`);
out('');
out('| Status | Count |');
out('|---|---|');
out(`| ✅ Both modes match | ${cs.match} |`);
out(`| ⚠️ Light matches, Dark differs | ${cs.darkOnlyMismatch} |`);
out(`| ❌ Value mismatch | ${cs.mismatch} |`);
out(`| ❌ Missing in iOS | ${cs.missingIos} |`);
